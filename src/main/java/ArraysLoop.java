public class ArraysLoop {
    public static void main(String[] args) {
        String[] cars = {"Volvo", "Bmw", "Ford"};
        for (int i = 0; i < cars.length; i++) {
            System.out.println(cars[i]);
        }


        String[] colors = {"red", "yellow", "white"};
        for (String c : colors) {
            System.out.println(c);
        }

    }
    }

